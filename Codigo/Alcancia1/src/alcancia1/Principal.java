    private void jButtonNuevaAlcanciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNuevaAlcanciaActionPerformed
        String nombre;
        String appPa;
        if(arch.lecturaBolean())JOptionPane.showMessageDialog(null,"Cantidad antigua de la cancia: "+cochi.CalcularTotalAhorrado());
        nombre=JOptionPane.showInputDialog("Ingrese su nombre: ");
        appPa=JOptionPane.showInputDialog("Ingrese su apellido Paterno: ");
        if(nombre==null&&appPa==null){// si se Cierra y no se ingresa nada en el JoptionPane
         nombre="NOMBRE";
         appPa="APELLIDO PATERNO";
        }else if(nombre==null)
        nombre="NOMBRE";
        else if(appPa==null)
        appPa="APELLIDO PATERNO";

        usuario.setNombre(nombre);
        usuario.setAppPaterno(appPa);
        cochi.NuevaAlcancia(usuario);
        cochi.setFechaInicio(formato.format(fecha).toString());
        arch.escritura(cochi);
        ActualizacionInicial();
        DeshabilitarBotones();
        jTextFieldMonedaAgregada.setText("Alcancia vacia");
    }//GEN-LAST:event_jButtonNuevaAlcanciaActionPerformed
