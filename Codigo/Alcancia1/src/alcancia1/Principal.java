     public void DeshabilitarBotones(){
        jButtonCincuenta.setEnabled(false);
        jButtonPeso.setEnabled(false);
        jButtonDosPesos.setEnabled(false);
        jButtonCincoPesos.setEnabled(false);
        jButtonDiesPesos.setEnabled(false);
    }
     
     public void ActualizacionInicial(){
     arrSeparar = arch.lectura().split(",");
    
    if(arrSeparar.length==8){
        
    cochi.setCincuentaCentavos(Integer.parseUnsignedInt(arrSeparar[0]));
    cochi.setPeso(Integer.parseUnsignedInt(arrSeparar[1]));
    cochi.setDosPesos(Integer.parseUnsignedInt(arrSeparar[2]));
    cochi.setCincoPesos(Integer.parseUnsignedInt(arrSeparar[3]));
    cochi.setDiesPesos(Integer.parseUnsignedInt(arrSeparar[4]));
    usuario.setNombre(arrSeparar[5]);
    usuario.setAppPaterno(arrSeparar[6]);
    cochi.setFechaInicio(arrSeparar[7]);
    cochi.GuardarPropietario(usuario);
    ActualizarInterface();
    }
    
    }
   
   public void ActualizarInterface(){
     // arrSeparar = arch.lectura().split(",");
      jTextFieldCincuenta.setText(arrSeparar[0]);
      jTextFieldPeso.setText(arrSeparar[1]);
      jTextFieldDosPesos.setText(arrSeparar[2]);
      jTextFieldCincoPesos.setText(arrSeparar[3]);
      jTextFieldDiesPesos.setText(arrSeparar[4]);
      jTextFieldNombre.setText(arrSeparar[5]+" "+arrSeparar[6]);
      jTextFieldFecha.setText(arrSeparar[7]);
      jTextFieldTotalDinero.setText("En la Alcancia hay "+cochi.CalcularTotalAhorrado()+" pesos");
      
    }
    
